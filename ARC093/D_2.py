A,B = map(int,input().split(" "))

s1 = "####################################################################################################\n\
.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#\n"
s2 = "....................................................................................................\n\
#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.#.\n"
black = '#' * 100 + '\n'
white = '.' * 100 + '\n'

cnt = (A-1) // 50
mer = (A - 1) % 50
if mer == 0:
	up = s1 * cnt + black
else:
	up = s1 * cnt + black + ('.#' * mer) + ('#' * (100 - 2*mer)) + '\n' + black

cnt = (B-1) // 50
mer = (B - 1) % 50

if mer == 0:
	down = s2 * cnt + white
else:
	down = s2 * cnt + white + ('#.' * mer) + ('.' * (100 - 2*mer)) + '\n' + white

print(str(len((up+down)) // 100) + " 100")
print(up + down)

